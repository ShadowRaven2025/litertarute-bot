            f"‚ù§Ô∏è *–û—Ç–ª–∏—á–Ω–æ!* –¢—ã –ø–æ—Å—Ç–∞–≤–∏–ª(–∞) –ª–∞–π–∫ –∫–Ω–∏–≥–µ:\n"
