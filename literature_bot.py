                "–•–æ—á–µ—Ç—Å—è —á–µ–≥–æ-—Ç–æ *–∑–∞—Ö–≤–∞—Ç—ã–≤–∞—é—â–µ–≥–æ*, *—Ä–∞—Å—Å–ª–∞–±–ª—è—é—â–µ–≥–æ*, *–≥—Ä—É—Å—Ç–Ω–æ–≥–æ*, *–≤–¥–æ—Ö–Ω–æ–≤–ª—è—é—â–µ–≥–æ*?"
